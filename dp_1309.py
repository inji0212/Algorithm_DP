# 사자를 가로새로 다 붙을 수 없다
# 한마리도 배치안하는 것도 경우수해딩
# 2 1 3 00 01 10 1+2
# 2 2 7 0000 0100 0110 1+4+2
# 2 3 15 000000 100000 100100 100110 1+6+8+2 

# 2 4 37 00000000 10000000 10010000 10011000 10011001 1+8+14+12+2
# -> dp[i]=2*dp[i-1] +dp[i-2]

n= int(input())
dp=[1]*(n+1)
dp[1]=3
for i in range(2,n+1):
  dp[i]=(2*dp[i-1]+dp[i-2])%9901
print(dp[n])
